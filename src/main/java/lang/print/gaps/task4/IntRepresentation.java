package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int x = 99;
        int y = 104;
        int z = 97;
        int g = 114;
        char c = (char) x;
        char h = (char) y;
        char a = (char) z;
        char r = (char) g;
        System.out.println(c);
        System.out.println(h);
        System.out.println(a);
        System.out.println(r);
    }
}
